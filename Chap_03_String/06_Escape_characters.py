# print new line in string  -> \n
a="I am Tony Stark\nFounder of Stark Industries"
print(a)


# tab space \t
a="I am Tony Stark\tFounder of Stark Industries"
print(a)

#use duble quotes in string    ->   \" enter \"
a="I am Tony Stark\nFounder of \"Stark Industries\""
print(a)


#blackslash ->  \b  remove char before that
a="I am Tony Stark\nFounder of A\bStark Industries"
print(a)
